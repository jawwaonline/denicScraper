// Funktion zum Generieren aller möglichen Kombinationen einer bestimmten Länge
function generateCombinations(letters, length) {
	let results = [];

	function helper(prefix, letters, length) {
		if (length === 0) {
			results.push(prefix);
			return;
		}
		for (let i = 0; i < letters.length; i++) {
			helper(prefix + letters[i], letters, length - 1);
		}
	}

	helper('', letters, length);
	return results;
}

