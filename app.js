const freedomains = [];

const wait = () => new Promise((resolve) => setTimeout(resolve, 400));
async function testDenicCombinations(combinations) {
	for (const combination of combinations) {
		await wait();
		const url = `https://webwhois.denic.de/?lang=de&query=${combination}`;

		try {
			const response = await fetch(url);
			if (response.ok) {
				const text = await response.text();
				// Analysieren Sie das erhaltene HTML, um den Status der Domain zu ermitteln
				if (!text.includes('ist bereits registriert')) {
					freedomains.push(combination);
				}

				console.log(
					`Response for ${combination}:`,
					text.includes('ist bereits registriert')
						? 'vergeben'
						: 'frei',
					freedomains
				);
			} else {
				console.error(`Error fetching ${url}:`, response.statusText);
				break;
			}
		} catch (error) {
			console.error(`Error fetching ${url}:`, error);
		}
	}
	console.log(freedomains);
}

